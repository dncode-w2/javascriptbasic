// Switch
// dice values 1 - 6

const dice = 0;

switch (dice) {
  case 0:
    console.log('you got zero');
    break;
  case 1:
    console.log('you got one');
    break;
  case 2:
    console.log('you got two');
    break;
  case 3:
    console.log('you got three');
    break;
  case 4:
    console.log('you got four');
    break;
  case 5:
    console.log('you got five');
    break;
  // will print if dice = 0;
  default:
    console.log('not in option');
}

